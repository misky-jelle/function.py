def add(a,b):
    answer= a+b
    return answer

def subtract(c,d):
    answer=c-d
    return answer

def multiply(e,f):
     answer=e*f
     return answer


def divide(g,h):
     answer=g/h
     return answer

def modulus(i,j):
     answer=i%j
     return answer

def divide_int(k,l):
     answer=k // l
     return answer
def exponent(m,n):  
     answer=m**n
     return answer  

def square(o):  
     answer=o*o
     return answer 

def cubed(p):  
     answer=p*p*p
     return answer  
